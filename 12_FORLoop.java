
/**
 *
 *      FOR Loop
 * */
public class Main {

	public static void main(String[] args) {

		for (int i = 0; i <= 20; i++) {
			if (i % 2 == 0) {
				System.out.println(i);
			}
		}

		System.out.println();

		for (int i = 20; i >= 0; i--) {
			if (i % 2 == 0) {
				System.out.println(i);
			}
		}
	}
}
