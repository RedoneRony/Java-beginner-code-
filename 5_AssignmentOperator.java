
/**
 *
 *  Assignment Operator
 * */
public class Main {

	public static void main(String[] args) {

		int salary = 20000;
		int promo = 5000;

		//salary = salary + promo;
		salary += promo;
		System.out.println(salary);

		// Example 2
		int num = 2;
		int multiplier = 5;

		//num = num * multiplier;
		num *= multiplier;
		System.out.println(num);
	}
}
